package first.step.numericos

fun main() {
    /*
    Byte:   1 byte - 8 bits
    Short:  2 byte - 16 bits
    Int:    4 bytes  - 32 bits
    long:   8 byte  - 64 bits
    Float:  4 bytes - 32 bits
    Double: 8 bytes - 64 bits
     */

    val cachorro = 10
    val gato: Int = 20
    val passaro = 10L //define que será long, o L precisa ser maiusculo

    val galinha = 14.5 // é definido como double
    var granja: Double = 20.8
    var jacaré = 7.5F//definido como float
    var pirarucu: Float  = 9.73F //complexo, mesmo sendo um tipo declarado como float, tem que botar o F



}