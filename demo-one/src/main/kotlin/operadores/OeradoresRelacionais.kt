package operadores

//Operadores relacionais sempre vai retorar um boleano
fun main() {

    //igual igual
    println("10 == 10 ís ${10 == 10}")
    //diferente
    println("10 != 7 ís ${10 != 7}")
    //Maior que
    println("5 > 2 ís ${5 > 2}")
    //Menor que
    println("5 < 2 ís ${5 < 2}")
    //Maior ou igual
    println("11 >= 10 ís ${11 >= 10}")
    //Menor ou igual
    println("5 <= 10 ís ${5 <= 10}")


}