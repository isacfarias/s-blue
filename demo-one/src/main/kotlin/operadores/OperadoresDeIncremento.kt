package operadores

fun main() {

    println("PRE incremento")
    var x = 8
    println(x)
    println(x++)
    println(x)

    println("POS incremento")
    var y = 10
    println(y)
    println(++y)


    println("PRE decremento")
    var b = 15
    println(b)
    println(b--)
    println(b)

    println("POS decremento")
    var c = 20
    println(c)
    println(--c)
}
